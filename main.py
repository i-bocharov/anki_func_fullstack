def add_words(words: Dict[str, str]) -> None:
    '''
    Добавляет новые пары слов и переводов в словарь в интерактивном режиме.
    Пользователь вводит слово и перевод поочерёдно.
    Для завершения ввода вводится служебное слово "СТОП" (регистр не важен).
    Функця изменяет переданный словарь по месту, не возвращает значения.
    '''
    # Сообщаем пользователю о способе завершения ввода.
    # Слово "СТОП" распознаётся независимо от регистра и пробелов.
    print('Для завершения ввода введите "СТОП" в любой момент.')

    while True:
        # Запрашиваем у пользователя ввод нового слова.
        word_input = input('Введите слово: ').strip()

        # Проверяем, не введено ли завершающее слово.
        # Сравниваем в нижнем регистре для универсальности.
        if word_input.lower() == STOP_WORD.lower():
            break

        # Запрашиваем перевод для введённого слова.
        translation_input = input('Введите перевод: ').strip()

        # Проверяем, не введено ли завершающее слово вместо перевода.
        # Если да - прерываем цикл, пару не добавляем.
        if translation_input.lower() == STOP_WORD.lower():
            break

        # Добавляем пару в словарь или обновляем существующую.
        # Словарь автоматически предотвращает дубликаты ключей.
        words[word_input] = translation_input


def show_all_words(words: Dict[str, str]) -> None:
    '''
    Выводит все пары слов и переводов из словаря в одну строку.
    Формат вывода: 'слово - перевод; слово - перевод; ...'.
    При пустом словаре выводит пустую строку. Функция не возвращает значения.
    '''
    # Проверяем, есть ли слова в словаре для вывода.
    # При пустом словаре выводим пустую строку.
    if not words:
        print()
        return

    # Список для хранения отформатированных пар слов и переводов.
    output_parts: List[str] = []

    # Формируем строку вида 'слово - перевод' для каждой пары.
    # Сохраняем оригинальный регистр слов без изменений.
    for word, translation in words.items():
        # Создаем отформатированную пару с разделителем ' - '.
        formatted_pair = f'{word} - {translation}'
        # Добавляем пару в список для последующего объединения.
        output_parts.append(formatted_pair)

    # Объединяем все пары через '; ' (точка с запятой и пробел).
    # Выводим результирующую строку одним сообщением на экран.
    formatted_output = '; '.join(output_parts)
    print(formatted_output)


def save_words(words: Dict[str, str], filename: str = 'words.txt') -> None:
    '''
    Сохраняет пары слов и переводов из словаря в текстовый файл.
    Каждая пара записывается в отдельной строке в формате 'слово,перевод'.
    Функция не возвращает значения, выводит сообщение о результате сохранения.
    '''
    # Выполняем валидацию пути к файлу для защиты от атак.
    _validate_file_path(filename)

    try:
        # Используем контекстный менеджер для гарантированного закрытия файла.
        # Это освобождает ресурсы даже при возникновении ошибок внутри блока.
        with open(filename, 'w', encoding='utf-8') as file:
            # Последовательно записываем каждую пару слова и перевода.
            for word, translation in words.items():
                # Формируем строку вида 'слово,перевод' для записи в файл.
                # Запятая служит разделителем для последующей загрузки.
                line_content = f'{word},{translation}\n'
                # Записываем сформированную строку в файл с кодировкой UTF-8.
                file.write(line_content)

            # Подсчитываем количество сохраненных пар слов и переводов.
            count_word_pairs = len(words)
            # Выводим сообщение пользователю о результате операции сохранения.
            print(f'Было сохранено {count_word_pairs} слов в файл {filename}')

    except IOError:
        # Обрабатываем ошибки ввода-вывода при записи в файл.
        print(f'Error: Unable to write to file "{filename}".')
        sys.exit(1)

